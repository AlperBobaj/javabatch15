package class7;

public class ForLoops {
    public static void main(String[] args) {


        /*
        prints number from 0 to 9
         */

        int number=0;
        while (number<10){
            System.out.println(number);
            number++;
        }
        for (number=0;number<10;number++){
            System.out.println(number);
        }
// using a for loop print odd number from 1 to 20;
    }
}
